#!/usr/bin/env python3
import sys
from itertools import combinations_with_replacement

def main():
    if len(sys.argv)!=4:
        print("0")
        return
    days=float(sys.argv[1])
    miles=float(sys.argv[2])
    receipts=float(sys.argv[3])

    coeffs=[-45.17827217759259,149.69977304461463,0.879221226869042,-0.9324448573374235,-6.390394247201812,-0.09267000087800348,-0.01448761439062407,-0.00014861711716966652,-0.0004375256490970966,0.003205035349582485,-0.3924502250454367,0.012632162640305977,-0.002535286316771468,1.553986225911845e-05,1.4242887777366625e-05,1.2568736364629502e-05,3.12484856831069e-08,7.472888750662642e-07,-2.252017104272804e-07,-1.9425639571371557e-06,0.032606275933970474,-0.0004118913535020585,0.00014264375660975794,-6.187762422983363e-06,-1.6614612659443143e-07,2.833339376563193e-07,6.64083158979715e-08,4.435744826051913e-09,-1.1106490941800606e-08,-3.6324753018631077e-09,-3.769893392037641e-10,1.505584440740887e-10,-4.119218203914901e-10,1.9058525873592145e-10,3.485852292787155e-10]
    vars=[days,miles,receipts]
    feats=[1.0]
    for deg in range(1,5):
        for combo in combinations_with_replacement(range(3),deg):
            val=1.0
            for idx in combo:
                val*=vars[idx]
            feats.append(val)
    base=sum(c*f for c,f in zip(coeffs,feats))

    cents=round(receipts-int(receipts),2)
    if abs(cents-0.49)<0.01 or abs(cents-0.99)<0.01:
        base+=-477.11692186827014

    print(f"{base:.2f}")

if __name__=="__main__":
    main()
